// One-time helper: outline the "PipGlyph" wordmark (Cinzel SemiBold 600)
// into a single SVG path so distributable logo SVGs carry no font
// dependency. The result is committed as WORDMARK in lib/brand/wordmark.ts
// — rerun only if the wordmark text or face changes.
//
//   node scripts/build-wordmark-path.mjs /path/to/Cinzel-600.ttf
//
// Cinzel ships no true lowercase (lowercase maps to small caps), so the
// outlined wordmark matches the live font-display rendering. Static
// instance source: https://fonts.googleapis.com/css?family=Cinzel:600
// (Cinzel is OFL-licensed; outlined text is fine to redistribute.)

import fs from "node:fs";
import opentype from "opentype.js";

const fontPath = process.argv[2];
if (!fontPath || !fs.existsSync(fontPath)) {
  console.error("usage: node scripts/build-wordmark-path.mjs <cinzel-600.ttf>");
  process.exit(1);
}

const buf = fs.readFileSync(fontPath);
const font = opentype.parse(
  buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength),
);
const TEXT = "PipGlyph";
const FONT_SIZE = 100;
const TRACKING = 0.05; // em — matches the header's tracking-wider feel

// Manual layout so we can add letterspacing between glyphs.
const glyphs = font.stringToGlyphs(TEXT);
const scale = FONT_SIZE / font.unitsPerEm;
let x = 0;
const paths = [];
for (let i = 0; i < glyphs.length; i++) {
  const glyph = glyphs[i];
  paths.push(glyph.getPath(x, 0, FONT_SIZE));
  x += glyph.advanceWidth * scale;
  if (i < glyphs.length - 1) {
    x += font.kerningPairs
      ? (font.getKerningValue(glyph, glyphs[i + 1]) || 0) * scale
      : 0;
    x += TRACKING * FONT_SIZE;
  }
}

const combined = paths.map((p) => p.toPathData(2)).join(" ");

// Measure the combined outline so consumers get a tight box.
const all = new opentype.Path();
for (const p of paths) all.commands.push(...p.commands);
const bb = all.getBoundingBox();

const result = {
  path: combined,
  x1: +bb.x1.toFixed(2),
  y1: +bb.y1.toFixed(2),
  x2: +bb.x2.toFixed(2),
  y2: +bb.y2.toFixed(2),
  width: +(bb.x2 - bb.x1).toFixed(2),
  height: +(bb.y2 - bb.y1).toFixed(2),
};

fs.writeFileSync(
  new URL("../lib/brand/wordmark.ts", import.meta.url),
  `// Generated by scripts/build-wordmark-path.mjs — do not edit by hand.
// "PipGlyph" outlined from Cinzel SemiBold (OFL) at ${FONT_SIZE}px with
// ${TRACKING}em tracking; baseline at y=0, bbox y ${result.y1}..${result.y2}.

export const WORDMARK = ${JSON.stringify(result, null, 2)} as const;
`,
);
console.log(
  `wordmark.ts written — ${result.width}×${result.height} units, ${combined.length} path chars`,
);
